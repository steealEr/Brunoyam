public class Rectangle extends Square{
    Rectangle(int length, int width) {
        super(length, width);
    }


    public void perimeter() {
        super.perimeter();
    }


    public void square() {
        super.square();
    }


    public void setLength(int length) {
        super.setLength(length);
    }


    public void setWidth(int width) {
        super.setWidth(width);
    }
}
